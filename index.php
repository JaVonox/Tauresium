	Hello and welcome to Tauresium! Tauresium is a multiplayer map game, inwhich you create a nation and rule it how you see fit, while expanding your influence over the world and fighting off any aggressors who might seek to take
	your land and people.
	<br><br>
	<b> What is Tauresium? </b>
	<br>
	Tauresium is an online map game inwhich a player takes control of a nation attempting to expand its influence over the modern world. The end goal of this project is to have a game which allows multiple players to
	interact with a world and each other. Each area in the game reflects a real life enviroment - and it is the intention to reflect the people and culture of these regions through in game text and
	gameplay mechanics.
	<br><br>
	It should be noted that for the best experience when playing this game you should ideally play in a group of 5 players (less players are possible but the game balance is focused on a 5 player campaign), with your other players
	starting at roughly the same time and playing roughly as often as each other.
	Aditionally, you should communicate with other players to decide the fate of your world, and work with or against them to define the borders of the earth.
	<br><br>
	<b> How do I play?</b>
	<br>
	To put it simply - create a world or join one using the create world or create country buttons, then login using the login box in the top right. You will start with three events, and generate more every so often depending on your game speed.
	These events will reward you with cultural, economic and military influence when completed, and your answers will change how much influence you recieve in each category in the future.
	This influence is spent to expand your nation over land, water or both (and others territories) using cultural, economic or military influence respectively. You should try to expand your borders as much as possible, to assert your
	authority on the map, bigger nations have more military power and can fight off aggressors with much more force. Keep your nation alive and communicate with your fellow players to stake out your claim on the world.
	<br><br>
	<b> Who is the winner? </b>
	<br>
	While there is no definitive winner of the game - the winner can be said to be the nation who controls the most territory at any point in time, or in a particularly militaristic game, whoever is left standing. Your world in
	Tauresium is intended to be your own space, and thus you decide who the winner is, or if there is one at all.
	<br><br>
